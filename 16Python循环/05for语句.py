# for 语句
# Python for 循环可以遍历任何可迭代对象，如一个列表或者一个字符串

sites = ["Baidu", "Google","Runoob","Taobao"]
for site in sites:
    print(site)

'''
Baidu
Google
Runoob
Taobao
'''


