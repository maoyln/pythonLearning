# 1. 算术运算符
# 用于数值运算：

# 运算符	名称	示例
# +	加法	x + y
# -	减法	x - y
# *	乘法	x * y
# /	除法	x / y
# %	取余	x % y
# **	幂运算	x ** y
# //	整除	x // y


# 2. 赋值运算符
# 用于给变量赋值：

# 运算符	示例	等价表达式
# =	x = 5	x = 5
# +=	x += 3	x = x + 3
# *=	x *= 3	x = x * 3
# //=	x //= 3	x = x // 3


# 3. 比较运算符
# 用于比较两个值：

# 运算符	说明	示例
# ==	等于	x == y
# !=	不等于	x != y
# >	大于	x > y
# <	小于	x < y


# 4. 逻辑运算符

# 用于逻辑条件的组合：

# and：当两个条件都为True时返回True
# or：任一条件为True返回True
# not：对结果取反


# 5. 身份运算符
# 用于比较对象是否相同（内存地址）：

# is：两个变量指向同一对象时返回True
# is not：指向不同对象返回True
# 6. 成员运算符

# 用于检测某个值是否存在于序列中：

# in：值存在返回True
# not in：值不存在返回True
# 7. 位运算符
# 按位进行运算：

# &：按位与 (AND)
# |：按位或 (OR)
# ^：按位异或 (XOR)
# ~：按位取反 (NOT)
# <<：向左移动
# >>：向右移动
# 运算符优先级

# 在Python中，运算符的执行遵循一定的优先级，括号内的运算符优先级最高：

print((6 + 3) - (6 + 3))  # 输出 0
print(100 + 5 * 3)        # 输出 

# 若运算符优先级相同，则从左到右依次执行：

print(5 + 4 - 7 + 3)  # 输出 5
# 熟悉这些运算符及其优先级顺序，有助于你写出更加简洁、高效的Python代码。