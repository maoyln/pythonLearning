# Python运算符的分类

# Python中的运算符分为以下几种类型：

# 算术运算符（Arithmetic Operators）
# 赋值运算符（Assignment Operators）
# 比较运算符（Comparison Operators）
# 逻辑运算符（Logical Operators）
# 身份运算符（Identity Operators）
# 成员运算符（Membership Operators）
# 位运算符（Bitwise Operators）


