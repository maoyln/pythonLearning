# Python字符串截取与切片操作

# 在学习Python时，经常需要从一个字符串中截取部分内容，这种操作称为切片（Slicing）。字符串的索引从0开始。


# 基本切片用法

# 使用方括号并指定起始索引和结束索引（不包含结束索引）：

b = "nihao, maoyl!"
print(b[2:5])  # 输出 hao

# 注意：第一个字符索引为0。


print('---------黄金分割线-----------')


# 从头开始切片
# 如果不指定起始索引，默认从字符串头部开始截取：
b = "nihao, maoyl!"
print(b[:5])  # 输出 nihao


print('---------黄金分割线-----------')


# 切片到字符串结尾
# 如果省略结束索引，Python 会自动截取到字符串的末尾：
b = "nihao, maoyl!"
print(b[7:])  # 输出 maoyl!


print('---------黄金分割线-----------')


# 负索引切片
# Python 支持使用负数索引从字符串末尾开始截取：
b = "nihao, maoyl!"
print(b[-6:-1])  # 输出 maoyl